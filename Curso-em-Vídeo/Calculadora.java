    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
       int N = (int) spinNumero.getValue();
       int resto = N % 2;
       int aoCubo = (int) Math.pow(N, 3);
       float raizQuadrada = (float) Math.sqrt(N);
       float raizCubica = (float) Math.cbrt(N);
       int absoluto = (int) Math.abs(N);
       
       txtRestoPor2.setText("Resto da Divis√£o por 2:");
       lblRestoPor2.setText(Integer.toString(resto));
       
       txtAoCubo.setText("Elevado ao Cubo:");
       lblAoCubo.setText(Integer.toString(aoCubo));
       
       txtRaizQuadrada.setText("Raiz Quadrada:");
       lblRaizQuadrada.setText(Float.toString(raizQuadrada));
       
       txtRaizCubica.setText("Raiz Cubica:");
       lblRaizCubica.setText(Float.toString(raizCubica));
     
       txtValorAbsoluto.setText("Valor Absoluto:");
       lblValorAbsoluto.setText(Integer.toString(absoluto));
        
       
       
        
    }//GEN-LAST:event_btnCalcularActionPerformed

